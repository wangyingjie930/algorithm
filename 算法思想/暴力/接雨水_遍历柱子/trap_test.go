import "testing"

func Test_trap(t *testing.T) {
	type args struct {
		height []int
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{name: "case1", args: args{height: []int{0, 1, 0, 2, 1, 0, 1, 3, 2, 1, 2, 1}}, want: 6},
		{name: "case1", args: args{height: []int{4, 2, 0, 3, 2, 5}}, want: 9},
		{name: "case1", args: args{height: []int{
			100000, 0, 99999, 0, 99998, 0, 99997, 0, 99996, 0, 99995, 0, 99994, 0, 99993, 0, 99992, 0, 99991, 0, 99990, 0, 99989, 0, 99988, 0,
			99987, 0, 99986, 0, 99985, 0, 99984, 0, 99983, 0, 99982, 0, 99981, 0, 99980, 0, 99979, 0, 99978, 0, 99977, 0, 99976, 0, 99975, 0,
			99974, 0, 99973, 0, 99972, 0, 99971, 0, 99970, 0, 99969, 0, 99968, 0, 99967, 0, 99966, 0, 99965, 0, 99964, 0, 99963, 0, 99962, 0,
			99961, 0, 99960, 0, 99959, 0, 99958, 0, 99957, 0, 99956, 0, 99955, 0, 99954, 0, 99953, 0, 99952, 0, 99951, 0, 99950, 0, 99949, 0, 99948,
			0, 99947, 0, 99946, 0, 99945, 0, 99944, 0, 99943, 0, 99942, 0, 99941, 0, 99940, 0, 99939, 0, 99938, 0, 99937, 0, 99936, 0, 99935, 0, 99934, 0,
			99933, 0, 99932, 0, 99931, 0, 99930, 0}}, want: 6997515},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := trap(tt.args.height); got != tt.want {
				t.Errorf("trap() = %v, want %v", got, tt.want)
			}
		})
	}
}
